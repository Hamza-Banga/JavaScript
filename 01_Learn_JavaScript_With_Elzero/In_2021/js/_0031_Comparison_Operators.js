/*
 
    Comparison Operators

    == Equal
    != Not Equal
    
    === Identical
    !== Not Odentical 

    > Larger Than
    >= Larger Than Or Equal
    
    < Smaller Than 
    <= Smaller Than Or Equal
    
 */

console.log(10 == "10");     // Compare Vlaue Only -> true
console.log(-10 == "-10");   // Compare Vlaue Only -> true
console.log(10 == "-10");    // Compare Vlaue Only -> false
console.log(10 != "10");     // Compare Vlaue Only -> false

console.log(10 === "10");    // Compare Vlaue + Type -> false
console.log(10 !== "10");    // Compare Vlaue + Type -> true
console.log(10 !== 100);     // Compare Vlaue + Type -> true


